class Copos {
  String _id;
  String _preco;
  String _tamanho;
  String _url;

  String get tamanho => _tamanho;

  set tamanho(String value) {
    _tamanho = value;
  }

  String get url => _url;

  set url(String value) {
    _url = value;
  }

  String get id => _id;

  set id(String value) {
    _id = value;
  }

  String get preco => _preco;

  set preco(String value) {
    _preco = value;
  }
}
